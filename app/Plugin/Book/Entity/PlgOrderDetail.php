<?php

namespace Plugin\Book\Entity;

class PlgOrderDetail extends \Eccube\Entity\AbstractEntity
{
    private $id;

    private $status;

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    public function getStatus()
    {
        return $this->status;
    }

    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }
}

